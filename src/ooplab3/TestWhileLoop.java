package ooplab3;

public class TestWhileLoop {
    public static void main(String[] args) {
////        While loop
        int n =1;
        while (n<=10){
            System.out.println("Hello");
            n++;


        }//while

//        do-while
        do {
            System.out.println("Hello");
            n++;
        } while (n<=10);




    }//main




}// class
